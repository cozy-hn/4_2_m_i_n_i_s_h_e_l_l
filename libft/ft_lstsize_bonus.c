#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	cnt;

	cnt = 1;
	if (!lst)
		return (0);
	while (lst->next)
	{
		cnt += 1;
		lst = lst->next;
	}
	return (cnt);
}
